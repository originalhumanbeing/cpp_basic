#include <iostream>
using namespace std;

int main()
{
	int num1=10;
	int num2=5;

	cout << "num1과 num2로 다양한 연산을 수행합니다. \n";
	cout << "num1+num2는 " << num1+num2 << "입니다. \n";
	cout << "num1-num2는 " << num1-num2 << "입니다. \n";
	cout << "num1*num2는 " << num1*num2 << "입니다. \n";
	cout << "num1/num2는 " << num1/num2 << "입니다. \n";
	cout << "num1%num2는 " << num1%num2 << "입니다. \n";

	return 0;
}